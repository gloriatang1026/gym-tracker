import { writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const BASE_URL = "https://247.fitness/app-api/cms/store/";
const OUTPUT_PATH = join(__dirname, "..", "lib", "generatedGymLocations.ts");

const REGION_NAME_MAP = {
  "New Territories District": "New Territories",
  "Kowloon District": "Kowloon",
  "Outlying Islands": "Outlying Islands",
  "Hong Kong Island": "Hong Kong Island",
  新界區: "New Territories",
  九龍區: "Kowloon",
  離島: "Outlying Islands",
  港島區: "Hong Kong Island",
};

function normalizeRegionName(areaName) {
  return REGION_NAME_MAP[areaName] || areaName.replace(/ District$/, "");
}

function titleCase(text) {
  return text
    .split(/(\s+|[-/()])/)
    .map((token) => {
      if (!token || /^(\s+|[-/()])$/.test(token)) return token;
      if (/^[A-Z0-9\s]+$/.test(token)) return token;
      return token.charAt(0).toUpperCase() + token.slice(1).toLowerCase();
    })
    .join("");
}

function normalizeBranchName(englishName, chineseName) {
  const normalizedEnglish = titleCase(englishName.trim());
  if (!chineseName) return normalizedEnglish;
  return `${normalizedEnglish} (${chineseName.trim()})`;
}

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

async function fetchJson(lang) {
  const response = await fetch(`${BASE_URL}?lang=${encodeURIComponent(lang)}`, {
    headers: {
      "tenant-id": "1",
      "content-type": "application/json",
      referer: "https://247.fitness/en/contact_us/stores",
      "user-agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64)",
    },
  });

  if (!response.ok) {
    throw new Error(`HTTP ${response.status} ${response.statusText}`);
  }

  return response.json();
}

async function main() {
  console.log("Fetching HK branch list from 247 Fitness...");
  const [enData, zhData] = await Promise.all([fetchJson("en-US"), fetchJson("zh-HK")]);

  assert(enData?.code === 0 && enData.data, "Unexpected English API response");
  assert(zhData?.code === 0 && zhData.data, "Unexpected Chinese API response");

  const enCountry = enData.data.countryNodes?.find((node) => node.countryCode === "HK");
  const zhCountry = zhData.data.countryNodes?.find((node) => node.countryCode === "HK");
  assert(enCountry, "English Hong Kong data not found");
  assert(zhCountry, "Chinese Hong Kong data not found");

  const zhStoreMap = new Map(
    zhCountry.areaNodeList.flatMap((area) =>
      (area.stores || []).map((store) => [store.storeId, store.storeName.trim()])
    )
  );

  const regions = enCountry.areaNodeList
    .map((area) => {
      const regionName = normalizeRegionName(area.areaName);
      const branches = [...new Set(
        (area.stores || [])
          .map((store) => normalizeBranchName(store.storeName, zhStoreMap.get(store.storeId)))
          .filter(Boolean)
      )];
      return { name: regionName, branches };
    })
    .filter((region) => region.branches.length > 0)
    .sort((a, b) => {
      const order = ["Hong Kong Island", "Kowloon", "New Territories", "Outlying Islands"];
      return order.indexOf(a.name) - order.indexOf(b.name);
    });

  const fileContents = `// Generated by scripts/scrape-247-branches.mjs\n// Source: https://247.fitness/en/contact_us/stores\n\nexport const GENERATED_GYM_REGIONS = ${JSON.stringify(regions, null, 2)};\n`;

  writeFileSync(OUTPUT_PATH, fileContents, "utf8");
  console.log(`Wrote ${OUTPUT_PATH}`);
  console.log(`${regions.length} regions generated.`);
}

main().catch((error) => {
  console.error("Branch scraper failed:", error);
  process.exit(1);
});
